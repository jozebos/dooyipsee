const allCards: TarotCard[] = [
  ...majorArcana,
  ...wands,
  ...cups,
  ...swords,
  ...pentacles,
];

export function getAllCards(): TarotCard[] {
  return allCards;
}

export function getCardBySlug(slug: string): TarotCard | undefined {
  return allCards.find((card) => card.slug === slug);
}

export function getCardsByArcana(arcana: Arcana): TarotCard[] {
  return allCards.filter((card) => card.arcana === arcana);
}

export function getCardsBySuit(suit: Suit): TarotCard[] {
  return allCards.filter((card) => card.suit === suit);
}

export function drawRandomCards(count: number): TarotCard[] {
  const shuffled = [...allCards].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

